# mcube-10 FPGA library

# AXI32 registers address offset
OFFSET_ID_VERSION = 0x00
OFFSET_SCRATCHPAD = 0x04
OFFSET_STATUS     = 0x08
OFFSET_LED        = 0x0c
OFFSET_PMOD0_MODE = 0x10
OFFSET_PMOD0_DATA = 0x14
OFFSET_PMOD1_MODE = 0x18
OFFSET_PMOD1_DATA = 0x1c
OFFSET_CKOK_DATA  = 0x20
OFFSET_AURORA_RESET   = 0x24
OFFSET_AURORA_PMAINIT = 0x28
OFFSET_AURORA_XRST    = 0x2c
OFFSET_EXT_ADDR    = 0x40
OFFSET_EXT_DATA    = 0x44

# HLS offset
HLS_CNTRL = 0x00
HLS_SNDD = 0x1000
HLS_RECD = 0x1000


# STATUS bit LSB/mask
STATUS_DSW_LSB       = 0
STATUS_PSW_LSB       = 4
STATUS_DSW_MASK      = (0x0f << STATUS_DSW_LSB)
STATUS_PSW_MASK      = (0x03 << STATUS_PSW_LSB)

